export const getContentByCategory = (categorySlug: string) => {
  return allContent.filter((item) => item.categorySlug === categorySlug);
};

export const getContentById = (id: number) => {
  return allContent.find((item) => item.id === id);
}

export const getTrendingContent = (limit: number = 5) => {
    return [...allContent].sort((a, b) => b.popularity - a.popularity).slice(0, limit);
}

